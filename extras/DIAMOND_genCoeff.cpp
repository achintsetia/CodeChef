int dArray[2000];

vector<pair<int, int> > vecArr[2000];

int expSum;

int calcTree( int start, int end, int level)
{
  if( start < end)
  {
    vecArr[level].push_back( pair<int, int>( dArray[start], dArray[end-1]));
    calcTree( start+1, end, level+1);
    calcTree( start, end-1, level+1);
  }
}

void analyse(int N)
{
  int count[N];
  for( int index=0; index<N; index++)
  {
    int number = dArray[index];
    cout << "Counts for " << number/1000 << ": ";
    for( int i=0; i<N; i++)
      count[i] = 0;

    for( int i=0; i<N; i+=2)
    {
      for( int j=0; j<vecArr[i].size(); j++)
      {
        if( vecArr[i][j].first == number)
          count[i]++;
        if( vecArr[i][j].second == number)
          count[i]++;
      }
    }

    double sum = 0;

    for( int i=0; i<N; i++)
    {
      sum += count[i] / pow(2.0, i+1);
      cout << count[i] << " ";
    }
    cout << "Sum: " << sum << endl;
  }
}

