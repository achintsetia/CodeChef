typedef long long int ull;
#define MOD 1000000007

int numDigits( int num)
{
  if( num == 0) return 0;
  return 1 + numDigits(num/10);
}

ull powInt(int num, int p)
{
  if( num == 0) return 0;
  if( p == 0) return 1;
  if( p == 1) return num;

  if( p%2 == 0)
  {
    ull x = powInt(num, p/2); return (x*x) % MOD;
  }
  else
    return (num*powInt(num, p-1)) % MOD;
}

ull sumFromTo(int start, int end)
{
  //cout << start << " " << end << endl;

  ull A = (start+end);
  ull B = (end-start+1);

  if(A%2 == 0) A/=2;
  else if(B%2 == 0) B/=2;

  ull x = ((A%MOD) * (B%MOD)) % MOD;
  //cout << "Sum: " << x << endl;
  return x;
}

ull Sum(int L, int R)
{
  ull ans = 0;
  ull high = R;
  ull low = L;

  while( high >= low)
  {
    int d = numDigits(high);
    ull X = max(powInt(10, d-1), low);
    ull A =  (d*sumFromTo( X, high)) % MOD;
    ans = (ans+A) % MOD;
    high = X-1;
  }

  return ans;

}

